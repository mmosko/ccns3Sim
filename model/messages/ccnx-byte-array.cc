CCNxByteArray::CCNxByteArray (size_t length, const uint8_t *bytes)
{
  m_bytes = StorageType (length, 0);
  for (int i = 0; i < length; ++i)
    {
      m_bytes[i] = bytes[i];
    }
}

CCNxByteArray::CCNxByteArray (const std::vector< uint8_t > &value)
{
  m_bytes = value;
}

CCNxByteArray::~CCNxByteArray ()
{
  // empty
}

size_t
CCNxByteArray::size () const
{
  return m_bytes.size ();
}

const uint8_t
CCNxByteArray::operator [] (size_t index) const
{
  return m_bytes[index];
}

bool
CCNxByteArray::operator == (const CCNxByteArray &other) const
{
  return m_bytes == other.m_bytes;
}

bool
CCNxByteArray::operator < (const CCNxByteArray &other) const
{
  return m_bytes < other.m_bytes;
}


std::ostream &
ns3::ccnx::operator << (std::ostream &os, CCNxByteArray const &array)
{
  os << "{ ByteArray (" << array.size () << "): 0x";
  for (size_t i = 0; i < array.size (); ++i)
    {
      os << hexStrings[array[i]];
    }
  os << " }";
  return os;
}
